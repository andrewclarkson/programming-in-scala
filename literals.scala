val five = 5
val hex_five = 0x5

val octal_five = Integer.parseInt("05", 8);
val binary_five = Integer.parseInt("101", 2);

println(five, hex_five, octal_five, binary_five)

val five_long = 5L
val five_float = 5f
val five_double = 5.0
val five_million = 5e6

println(five_long, five_float, five_double, five_million)
